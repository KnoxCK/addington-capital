//small image linking to large image
$("#property").click(function(e) {
    e.preventDefault();
      $('html,body').animate({
          scrollTop: $(".property-wrapper").offset().top - 100},
          'slow');
      $(".harmsworth").removeClass("hidden");
  	  $(".randall").addClass("hidden");
});
